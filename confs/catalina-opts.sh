echo "CATALINA_OPTS=\"\$CATALINA_OPTS -Ddb.port=\$DB_PORT\"" >> $CATALINA_OPTS
echo "CATALINA_OPTS=\"\$CATALINA_OPTS -Ddb.name=\$DB_NAME\"" >> $CATALINA_OPTS
echo "CATALINA_OPTS=\"\$CATALINA_OPTS -Ddb.UsrName=\$DB_USR_NAME\"" >> $CATALINA_OPTS
